public class Variable {
    public static void main(String[] args) {
        byte core = 8;
        short sizeHdd = 1;
        int sizeSsd = 1000;
        long number = 1_000_000_000;
        float piFloat = 3.141_592f;
        double pi = 3.141_592_653;
        char ch = 'A';
        boolean answer = true;

        System.out.println(core + " " + sizeHdd + " " + sizeSsd + " " + number + " "+ piFloat + " " + pi + " " + ch + " " + answer);
    }
}